public class Relacionales {
    public static void main(String[] args) {
        
        final int X = 6;
        final int Y = 7;

        System.out.println("X == Y -> " + (X == Y));
        System.out.println("X != Y -> " + (X != Y));
        System.out.println("X > Y -> " + (X > Y));
        System.out.println("X < Y -> " + (X < Y));
        System.out.println("X >= Y -> " + (X >= Y));
        System.out.println("X <= Y -> " + (X <= Y));
    }
}
